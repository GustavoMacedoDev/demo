CREATE TABLE user_entity (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  email VARCHAR(255),
  password VARCHAR(255),
  profile VARCHAR(255) NOT NULL,
  CONSTRAINT pk_user_entity PRIMARY KEY (id)
);

ALTER TABLE user_entity ADD CONSTRAINT uc_user_entity_email UNIQUE (email);

CREATE TABLE product (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255),
  price DOUBLE PRECISION,
  status VARCHAR(255) NOT NULL,
  CONSTRAINT pk_product PRIMARY KEY (id)
);

ALTER TABLE product ADD CONSTRAINT uc_product_name UNIQUE (name);

CREATE TABLE category (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255),
  CONSTRAINT pk_category PRIMARY KEY (id)
);

ALTER TABLE category ADD CONSTRAINT uc_category_name UNIQUE (name);